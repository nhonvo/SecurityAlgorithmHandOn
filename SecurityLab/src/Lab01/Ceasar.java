    char EncodeCharacter(char c, int k){
        if(!Character.isLetter(c))
            return c;
        return (char)((((Character.toUpperCase(c)-'A')+k)%26 +26)%26+'A');
    }
    private String Encode(String br, int k){
        String kq = "";
        int n = br.length();
        for(int i = 0; i < n; i++){
            kq+=EncodeCharacter(br.charAt(i),k);
        }
        return kq;
    }
